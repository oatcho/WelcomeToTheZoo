package com.company;

public class Dog {

    private String name;
    private String breed;
    private int age;

    public void setName(String dogName) {
        name = dogName;
    }

    public String getName() {
        return name;
    }

    public void setBreed(String dogBreed) {
        breed = dogBreed;
    }

    public String getBreed() {
        return breed;
    }

    public void setAge(int dogAge) {
        age = dogAge;
    }

    public int getAge() {
        return age;

}}
